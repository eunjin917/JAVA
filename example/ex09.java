package example;

public class ex09 {
    public static void main(String[] args) {
        // 산술 : + - * / %

        int i = 10;
        int j = 3;
        
        System.out.println(i + j);
        System.out.println(i - j);
        System.out.println(i * j);
        System.out.println(i / j);  // 나눗셈의 몫
        System.out.println(i % j);  // 나눗셈의 나머지
    }
}
