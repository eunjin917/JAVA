package example;

public class ex01 {
    // public : 예약어(키워드) 접근지정자 - 모든 클래스에서 접근가능
    // class : 예약어(키워드) 클래스 선언 시 사용
    public static void main(String[] args) {
        // static : 정적 변수 or 메서드를 지정할 때 사용
        //        : 프로그램 시작 시 고정된 메모리 주소에 할당됨
        //        : 프로그램 시작점이나 중요한 메서드/변수에 사용함
        System.out.println("Hello Java!");
        // println : 콘솔에 문자열을 출력하는 메서드 (print line)
    }
}
